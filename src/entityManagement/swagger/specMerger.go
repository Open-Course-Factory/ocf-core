// MergeSpecs combine les deux sources de documentation
func (sm *SwaggerSpecMerger) MergeSpecs() map[string]any {
	// 1. CrÃ©er une spec de base valide
	baseSpec := sm.createValidBaseSpec()

	// 2. Ajouter les schÃ©mas manquants communs
	sm.addCommonSchemas(baseSpec)

	// 3. RÃ©cupÃ©rer et merger la documentation swag
	swagSpec := sm.getSwagSpecSafely()
	log.Printf("ğŸ” DEBUG: getSwagSpecSafely returned nil=%v", swagSpec == nil)
	if swagSpec != nil {
		log.Printf("ğŸ” DEBUG: baseSpec paths before addSwagContent: %d", len(sm.getPaths(baseSpec)))
		sm.addSwagContent(baseSpec)
		log.Printf("ğŸ” DEBUG: baseSpec paths after addSwagContent: %d", len(sm.getPaths(baseSpec)))
	}

	// 4. Ajouter la documentation auto-gÃ©nÃ©rÃ©e
	autoSpec := sm.docGenerator.GenerateOpenAPISpec()
	if autoSpec != nil {
		sm.addAutoGeneratedContent(baseSpec, autoSpec)
	}

	// 5. Diagnostic et correction des schÃ©mas manquants
	sm.diagnoseAndFixSchemaIssues(baseSpec)

	// 5. Convertir les rÃ©fÃ©rences Swagger 2.0 vers OpenAPI 3.0
	sm.convertSwagger2References(baseSpec)

	// 6. Valider la spec finale
	sm.validateAndFixSpec(baseSpec)

	log.Printf("âœ… Generated valid OpenAPI spec with %d paths",
		len(sm.getPaths(baseSpec)))

	return baseSpec
}

